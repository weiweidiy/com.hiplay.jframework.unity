    private const string ViewDirectoryKey = PrefsPrefix + "ViewDirectory";
    private const string DtoDirectoryKey = PrefsPrefix + "DtoDirectory";
    private const string SceneStateDirectoryKey = PrefsPrefix + "SceneStateDirectory";
    private const string ControllerRegistryFileKey = PrefsPrefix + "ControllerRegistryFile";
    private const string ModelRegistryFileKey = PrefsPrefix + "ModelRegistryFile";
    private const string ViewRegistryFileKey = PrefsPrefix + "ViewRegistryFile";
    private const string SceneStateRegistryFileKey = PrefsPrefix + "SceneStateRegistryFile";

    private string sceneStateName = string.Empty;
    private bool createSceneState = true;
    private string sceneStateRegistryFile = DefaultSceneStateRegistryFile;

    private Vector2 scrollPosition;
    private string namespaceName = DefaultNamespace;

    private string controllerName = string.Empty;
    private string modelName = string.Empty;
    private string viewName = string.Empty;

    private bool createController = true;
    private bool createModel = true;
    private bool createView = true;

    private string controllerDirectory = DefaultControllerDir;
    private string modelDirectory = DefaultModelDir;
    private string viewDirectory = DefaultViewDir;
    private string dtoDirectory = DefaultDtoDir;
    private string sceneStateDirectory = DefaultSceneStateDir;

