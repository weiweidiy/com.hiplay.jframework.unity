    private string prefabName = string.Empty;
    private bool prefabNameCustomized;
    private bool generateViewData = true;

    [MenuItem("JFrameworkTools/业务模块代码生成器")]
    public static void ShowWindow()
    {
        var window = GetWindow<MvcModuleScaffolder>("MVC代码生成器");
        window.minSize = new Vector2(640f, 640f);
    }

    private void OnEnable()
    {
        LoadPreferences();
        RefreshDtoOptions();
        RefreshSceneStateOptions();
        TryUpdateModelKeyFieldFromDto(dtoType);
        UpdateDefaultPrefabName(force: true);
    }

    private void OnDisable()
    {
        SavePreferences();
    }

    private void OnGUI()
    {
        scrollPosition = EditorGUILayout.BeginScrollView(scrollPosition);

        EditorGUILayout.LabelField("MVC 模块脚手架", EditorStyles.boldLabel);
        EditorGUILayout.HelpBox("生成 Controller / Model / View 模板，并自动更新三个 RegistryModule 注册段。", MessageType.Info);

        var newNamespaceName = EditorGUILayout.TextField("命名空间", namespaceName);
        if (!string.Equals(newNamespaceName, namespaceName, StringComparison.Ordinal))
        {
            namespaceName = newNamespaceName;
            SavePreferences();
        }

        EditorGUILayout.Space(8f);
        DrawCreateOptions();

        EditorGUILayout.Space(8f);
        DrawNameOptions();

        EditorGUILayout.Space(8f);
        DrawPathOptions();

        EditorGUILayout.Space(8f);
        DrawRegistryFileOptions();

        if (createModel)
        {
            EditorGUILayout.Space(8f);
            DrawModelOptions();
        }

        if (createView)
        {
            EditorGUILayout.Space(8f);
            DrawViewOptions();
        }

        EditorGUILayout.Space(16f);

        var disableGenerate =
            (createController && string.IsNullOrWhiteSpace(controllerName)) ||
            (createModel && string.IsNullOrWhiteSpace(modelName)) ||
            (createView && string.IsNullOrWhiteSpace(viewName)) ||
            (createSceneState && string.IsNullOrWhiteSpace(sceneStateName));

        using (new EditorGUI.DisabledScope(disableGenerate))
        {
            if (GUILayout.Button("生成并自动注册", GUILayout.Height(36f)))
            {
                Generate();
            }
        }

        EditorGUILayout.Space(8f);
        if (GUILayout.Button("仅刷新注册类", GUILayout.Height(28f)))
        {
            SyncAllRegistrySections();
            AssetDatabase.Refresh();
            EditorUtility.DisplayDialog("完成", "注册类已刷新。", "确定");
        }

        EditorGUILayout.EndScrollView();
    }

